# slicing de listas 

# creemos una lista 

carrito_de_compras = ['Laptop', 'Teclado', 'Mouse', 'Tarjeta Grafica']

# hagamos slicing 
print(carrito_de_compras[0]) # mostramos el primer elemento de la lista 

# slicing 
print(carrito_de_compras[0:3]) # de Laptop a Mouse [inicio : fin - 1]

# otro slicing 
print(carrito_de_compras[::-1]) # invertir la lista 




# otras aplicaciones de listas 

#Â 1. crear una lista en un rango
lista_de_100 = list(range(100))

print(lista_de_100)

# suma de una lista
suma = sum(lista_de_100) # la suma de los elementos de la lista
maximo = max(lista_de_100) # el maximo valor de la lista
minimo = min(lista_de_100) # el minimo valor de la lista

print(suma)
print(maximo)
print(minimo)

# total de elementos de una lista
total_elementos = len(lista_de_100)
print(f"el total de elementos de la lista es: {total_elementos}")

# operaciones matematicas basicas en python 

# suma
print(2+2)

# resta 
print(5-2)

# multiplicacion
print(5*2)

# division 
print(10/2)

# otras operaciones

# Potencia
print(2**2) # 2 elevado a 2

# division sin decimal
print(2//6)

# modulo
print(4 % 2 )

# redondear
print(round(3.1)) # redondea hacia abajo
print(round(3.7)) # redondea hacia arriba

# valor absoluto de 20
print(abs(-20)) 
my_list = [1, 2, 3, 4, 5] # creamos una lista 
sum = 0 # iniciamos la suma en cero 

for number in my_list: # recorremos la lista y number en cada iteracion 
    print(number) # mostramos cada iteracion 
    sum += number # vamos sumando cada numero en cada iteracion 


print(sum) # mostramos la suma final